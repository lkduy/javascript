// Bài tập
// Khai báo 2 biến x, y nhận giá trị số bất kì
// Khai báo các biến khác và lần lượt gán giá trị tổng, hiệu, tích, thương của x, y cho từng biến.

var x = 1;
var y = 2;

// Tính tổng:
var tong = x + y;
console.log(tong);

// Tính hiệu:
var hieu = x - y;
console.log(hieu);

// Tính tích:
var tich = x * y;
console.log(tich);

// Tính thương:
var thuong = x/y;
console.log(thuong);